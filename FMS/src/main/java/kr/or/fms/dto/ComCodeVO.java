package kr.or.fms.dto;

public class ComCodeVO {
	
	private String com_group_code;
	private String com_code;
	private String com_name;
	private String com_use;
	private int com_seq;
	
	public String getCom_group_code() {
		return com_group_code;
	}
	public void setCom_group_code(String com_group_code) {
		this.com_group_code = com_group_code;
	}
	public String getCom_code() {
		return com_code;
	}
	public void setCom_code(String com_code) {
		this.com_code = com_code;
	}
	public String getCom_name() {
		return com_name;
	}
	public void setCom_name(String com_name) {
		this.com_name = com_name;
	}
	public String getCom_use() {
		return com_use;
	}
	public void setCom_use(String com_use) {
		this.com_use = com_use;
	}
	public int getCom_seq() {
		return com_seq;
	}
	public void setCom_seq(int com_seq) {
		this.com_seq = com_seq;
	}
	
	
}
