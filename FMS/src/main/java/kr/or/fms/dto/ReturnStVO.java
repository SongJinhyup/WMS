package kr.or.fms.dto;

public class ReturnStVO {
	
	private String return_code;
	private String hq_goods_code;
	private String return_receiver;
	private String return_sender;
	private String return_pro_st;
	private int return_price;
	
	public String getReturn_code() {
		return return_code;
	}
	public void setReturn_code(String return_code) {
		this.return_code = return_code;
	}
	public String getHq_goods_code() {
		return hq_goods_code;
	}
	public void setHq_goods_code(String hq_goods_code) {
		this.hq_goods_code = hq_goods_code;
	}
	public String getReturn_receiver() {
		return return_receiver;
	}
	public void setReturn_receiver(String return_receiver) {
		this.return_receiver = return_receiver;
	}
	public String getReturn_sender() {
		return return_sender;
	}
	public void setReturn_sender(String return_sender) {
		this.return_sender = return_sender;
	}
	public String getReturn_pro_st() {
		return return_pro_st;
	}
	public void setReturn_pro_st(String return_pro_st) {
		this.return_pro_st = return_pro_st;
	}
	public int getReturn_price() {
		return return_price;
	}
	public void setReturn_price(int return_price) {
		this.return_price = return_price;
	}
	
	

}
